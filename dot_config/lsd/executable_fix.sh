add_icon_to_name() {
  local key=$1 icon=$2
  if [ -z "$(sed -nr "/ +$key: $icon/p" icons.yaml)" ]; then
    sed -i "" -r "/^name:\$/a \\
  $key: $icon
    " icons.yaml
    echo "add icon config to name '  $key: $icon '"
  fi
}

